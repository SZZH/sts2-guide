#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const repoRoot = path.resolve(new URL('..', import.meta.url).pathname);
const defaultBundleDir = '/Users/jiaojian/Desktop/sts2/sts2整理过的文件/sts2_site_asset_bundle_v3/named_assets';
const bundleDir = process.env.STS2_BUNDLE_DIR || defaultBundleDir;
const outputFile = path.join(repoRoot, 'shared', 'relicPotionData.generated.ts');
const reportFile = path.join(repoRoot, 'relic-potion-import-report.md');
const wikiMapFile = path.join(repoRoot, 'shared', 'relicPotionWikiMap.generated.json');
const relicImagePrefix = process.env.STS2_RELIC_IMAGE_PREFIX || 'relics-v20260310-wiki-r1';
const potionImagePrefix = process.env.STS2_POTION_IMAGE_PREFIX || 'potions-v20260310-wiki-r1';

const relicFile = path.join(bundleDir, 'relic_data_full.json');
const potionFile = path.join(bundleDir, 'potion_data_full.json');
const wikiMapRaw = mustReadJson(wikiMapFile);
const RELIC_WIKI_MAP = wikiMapRaw.relics ?? {};
const POTION_WIKI_MAP = wikiMapRaw.potions ?? {};

function mustReadJson(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing file: ${filePath}`);
  }
  return JSON.parse(fs.readFileSync(filePath, 'utf-8'));
}

function cleanupRichText(text = '') {
  return String(text)
    .replace(/\[[^\]]+\]/g, '')
    .replace(/\{([^}]+)\}/g, '$1')
    .replace(/\s+/g, ' ')
    .trim();
}

function normalizeRarity(raw, fallback = 'Common') {
  const value = String(raw || '').trim();
  if (!value) return fallback;
  const map = new Map([
    ['Starter', 'Starter'],
    ['Common', 'Common'],
    ['Uncommon', 'Uncommon'],
    ['Rare', 'Rare'],
    ['Shop', 'Shop'],
    ['Ancient', 'Ancient'],
  ]);
  return map.get(value) || fallback;
}

function normalizeName(row, fallback) {
  return String(row.title_en || '').trim() || fallback;
}

function toRelicsRows(rows) {
  return rows
    .filter((row) => row && row.slug && row.class_name)
    .map((row) => {
      const slug = String(row.slug);
      const wiki = RELIC_WIKI_MAP[slug];
      if (!wiki?.imageFileName) return null;
      const name = wiki?.name?.trim() || normalizeName(row, row.slug);
      const description = wiki?.description?.trim() || cleanupRichText(row.description_en || '');
      const imageFileName = wiki.imageFileName.trim();
      return {
        slug,
        name,
        rarity: normalizeRarity(row.rarity, 'Common'),
        character: 'Neutral',
        description,
        image: `https://assets.sts2guide.com/assets/sts2/${relicImagePrefix}/${imageFileName}`,
      };
    })
    .filter(Boolean)
    .sort((a, b) => a.name.localeCompare(b.name));
}

function toPotionsRows(rows) {
  return rows
    .filter((row) => row && row.slug && row.class_name)
    .map((row) => {
      const slug = String(row.slug);
      const wiki = POTION_WIKI_MAP[slug];
      if (!wiki?.imageFileName) return null;
      const name = wiki?.name?.trim() || normalizeName(row, row.slug);
      const description = wiki?.description?.trim() || cleanupRichText(row.description_en || '');
      const imageFileName = wiki.imageFileName.trim();
      return {
        slug,
        name,
        rarity: normalizeRarity(row.rarity, 'Common'),
        character: 'Neutral',
        description,
        image: `https://assets.sts2guide.com/assets/sts2/${potionImagePrefix}/${imageFileName}`,
      };
    })
    .filter(Boolean)
    .sort((a, b) => a.name.localeCompare(b.name));
}

function renderTs(relics, potions) {
  return `/* eslint-disable */
// Auto-generated by scripts/generate-relic-potion-data.mjs
// Source: ${bundleDir}

export type GeneratedRelic = {
  slug: string;
  name: string;
  rarity: 'Starter' | 'Common' | 'Uncommon' | 'Rare' | 'Shop' | 'Ancient';
  character: 'Neutral' | 'Ironclad' | 'Silent' | 'Regent' | 'Necrobinder' | 'Defect';
  description: string;
  image?: string;
};

export type GeneratedPotion = {
  slug: string;
  name: string;
  rarity: 'Common' | 'Uncommon' | 'Rare';
  character: 'Neutral' | 'Ironclad' | 'Silent' | 'Regent' | 'Necrobinder' | 'Defect';
  description: string;
  image?: string;
};

export const RELICS_GENERATED: GeneratedRelic[] = ${JSON.stringify(relics, null, 2)} as GeneratedRelic[];

export const POTIONS_GENERATED: GeneratedPotion[] = ${JSON.stringify(potions, null, 2)} as GeneratedPotion[];
`;
}

function renderReport(relics, potions) {
  const relicWithImage = relics.filter((row) => row.image).length;
  const potionWithImage = potions.filter((row) => row.image).length;
  return `# Relic/Potion Import Report

- Source: \`${bundleDir}\`
- Generated at: ${new Date().toISOString()}

## Relics
- Total rows: ${relics.length}
- Rows with image: ${relicWithImage}
- Rows without image: ${relics.length - relicWithImage}
- Image prefix: ${relicImagePrefix}

## Potions
- Total rows: ${potions.length}
- Rows with image: ${potionWithImage}
- Rows without image: ${potions.length - potionWithImage}
- Image prefix: ${potionImagePrefix}
`;
}

const relicRowsRaw = mustReadJson(relicFile);
const potionRowsRaw = mustReadJson(potionFile);
const relics = toRelicsRows(relicRowsRaw);
const potions = toPotionsRows(potionRowsRaw);

fs.writeFileSync(outputFile, renderTs(relics, potions), 'utf-8');
fs.writeFileSync(reportFile, renderReport(relics, potions), 'utf-8');

console.log(`Generated ${outputFile}`);
console.log(`Generated ${reportFile}`);
console.log(`Relics: ${relics.length}, Potions: ${potions.length}`);
